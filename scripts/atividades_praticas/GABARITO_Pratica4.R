### Referentes ao video 9

# 1. Qual a diferenca entre distribuicao teorica e distribuicao empirica? Os dados que coletamos sao instancias de distribuicao teorica ou distribuicao empirica?

### RESPOSTA: Os dados observados sao instancias de distribuicao empirica, pois o coletamos experimentalmente. Ja as distribuicoes teoricas sao distribuicoes teoricamente previstas considerando-se determinados parametros.
  
# 2. Quais sao as caracteristicas de uma distribuicao normal? Quais sao seu parametros?

### RESPOSTA: A distribuicao normal eh simetrica e suas observacoes assumem valores numericos continuos. Seus parametros sao a media e o desvio-padrao.

# 3. Imagine um experimento que meca o tempo de leitura de palavras. Apos analisar os dados e chegar a conclusao de que eles seguem uma distribuicao semelhante a uma normal, o pesquisador reporta que o tempo medio de leitura foi de 678 milissegundos, com desvio-padrao de 37. Resolva:
  
#  (a) Que intervalo de valores, em milissegundos, concentra 68% dos dados observados?

### RESPOSTA: O intervalo que vai entre a media e +/- o desvio-padrao, o que nesse caso corresponde ao intervalo entre 641 e 715.

678 - 37
678 + 37

#  (b) Que intervalo de valores, em milissegundos, concentra 95% dos dados observados?

### RESPOSTA: O intervalo que vai entre a media e +/- duas vezes o desvio-padrao, o que nesse caso corresponde ao intervalo entre 604 e 752.

678 - 2*37
678 + 2*37
